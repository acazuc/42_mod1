#include "mod1.h"

void	scenar_wave(t_env *env)
{
	int		i;

	i = 0;
	while (i < MAP_SIZE)
	{
		env->water[0][i] = MAP_SIZE / 2 - env->map[0][i];
		i++;
	}
}
