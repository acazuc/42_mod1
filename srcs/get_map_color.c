#include "mod1.h"

int		get_map_color(int height)
{
	int		r;
	int		g;
	int		b;

	r = 255;
	if (r < 0)
		r = 0;
	if (r > 255)
		r = 255;
	g = 255 - (height / (double)MAP_SIZE / 2. * 255.);
	if (g < 0)
		g = 0;
	if (g > 255)
		g = 255;
	b = 255 - (height / (double)MAP_SIZE * 255.);
	if (b < 0)
		b = 0;
	if (b > 255)
		b = 255;
	return ((r << 16) + (g << 8) + b);
}
